szam=int(input('Kérek egy számot 1-7 között: '))
if szam==1:
    print('Hétfő')
elif szam==2:
    print('Kedd')
elif szam==3:
    print('Szerda')
elif szam==4:
    print('Csütörtök')
elif szam==5:
    print('Péntek')
elif szam==6:
    print('Szombat')
elif szam==7:
    print('Vasárnap')
else:
    print('Hibás számot adtál meg :(')